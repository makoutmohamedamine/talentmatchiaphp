export function translate(lang, key) {
  const parts = key.split('.');
  let value = translations[lang] ?? translations.fr;
  for (const part of parts) {
    value = value?.[part];
    if (value === undefined) {
      let fallback = translations.fr;
      for (const p of parts) {
        fallback = fallback?.[p];
      }
      return fallback ?? key;
    }
  }
  return value;
}
